void miniMaxSum(vector<int> arr) {  
    unsigned int sum_of_all_elements = accumulate(arr.begin(), arr.end(), 0);
    unsigned int minimum_element = *min_element(arr.begin(), arr.end());
    unsigned int maximum_element = *max_element(arr.begin(), arr.end());
    unsigned int minimum_sum = sum_of_all_elements - maximum_element;
    unsigned int maximum_sum = sum_of_all_elements - minimum_element;
    cout << minimum_sum << " " << maximum_sum << endl;
}

